/** Resolve an icon name to its inner SVG markup, falling back to `default`. */
export function resolveIconPath(name: string | undefined | null): string {
  if (!name) return ICON_PATHS.default;
  return ICON_PATHS[name] ?? ICON_PATHS.default;
}

/** True when the registry has an explicit entry for `name` (not just the fallback). */
export function hasIcon(name: string | undefined | null): boolean {
  return !!name && name in ICON_PATHS;
}
